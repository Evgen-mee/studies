# по умолчанию использует рандомизацию хешей для некоторых типов
# чтобы сделать хеш-значения менее предсказуемыми Это делает и позволяет избежать возникновения атак
# Злоумышленники могут использовать слабый алгоритм хеширования,
# чтобы преднамеренно создавать коллизии хешей, перегружая сервер и делая его недоступным.

# Ни в коем случае нельзя сохранять хеш-значения, возвращаемые функцией hash(),
# в базу данных для дальнейшего использования.
# Во-первых, они могут быть различными при каждом новом запуске программы,
# во-вторых, алгоритм хеширования может поменяться в новых версиях Python.

# Мы можем отключить рандомизацию для хешей,
#  установив фиксированное начальное значение для переменной окружения PYTHONHASHSEED